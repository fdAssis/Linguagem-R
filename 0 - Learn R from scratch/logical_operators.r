# Usage of logical operators
number1 <- 10
number2 <- 3

# Element wise logical AND between number1 and number2
number1 & number2 

# Logical AND between number1 and number2
number1 && number2

# Element wise logical OR between number1 and number2
number1 | number2

# Element wise logical OR between number1 and number2
number1 || number2

# Not of number1
!number1

# XOR of number1 and number2 (AND > NOR > OR)
xor(number1, number2) 

