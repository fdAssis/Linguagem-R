# Using arithmetic operators on variables
number1 <- 10
number2 <- 3

# Addition
number1 + number2

# Subtraction
number1 - number2

# Multiplication
number1 * number2

# Exponent 
number1 ^ number2

# Modulus
number1 %% number2

# Integer Division
number1 %/% number2

# Using arithmetic operators on vectors
vector1 <- c(5, 10, 15)
vector2 <- c(3, 6, 9)

# Addition
vector1 + vector2

# Subtraction
vector1 - vector2

# Multiplication
vector1 * vector2

# Division 
vector1 / vector2

# Exponent 
vector1 ^ vector2

# Modulus
vector1 %% vector2

# Integer Division
vector1 %/% vector2

